#include <stdio.h>

int main() {
    int a = 10, b = 5, c = 2, d = 3;
    int result;

    // ১. গুণ ও ভাগের কাজ যোগের আগে হয় (High Precedence)
    result = a + b * c; 
    // হিসাব: ১০ + (৫ * ২) = ২০
    printf("Example 1 (a + b * c)     : %d\n", result);

    // ২. ব্র্যাকেটের কাজ সবার আগে হয় (Highest Precedence)
    result = (a + b) * c; 
    // হিসাব: (১০ + ৫) * ২ = ৩০
    printf("Example 2 ((a + b) * c)   : %d\n", result);

    // ৩. একই লেভেলের অপারেটর হলে বাম থেকে ডানে কাজ করে (Associativity)
    result = a / b * d; 
    // হিসাব: (১০ / ৫) * ৩ = ৬
    printf("Example 3 (a / b * d)     : %d\n", result);

    // ৪. রিলেশনাল অপারেটরের চেয়ে অ্যারিথমেটিক অপারেটরের ক্ষমতা বেশি
    result = a + b > c * d; 
    // হিসাব: (১০ + ৫) > (২ * ৩) => ১৫ > ৬ (যা সত্য বা ১)
    printf("Example 4 (a + b > c * d) : %d\n", result);

    // ৫. লজিক্যাল AND (&&) এর ক্ষমতা লজিক্যাল OR (||) এর চেয়ে বেশি
    result = 1 || 0 && 0; 
    // হিসাব: ১ || (০ && ০) => ১ || ০ = ১
    printf("Example 5 (1 || 0 && 0)   : %d\n", result);

    return 0;
} 

